inline std::string extract_pinyin_initials(const std::string& utf8) {
    std::string result;
    size_t i = 0;
    while (i < utf8.size()) {
        uint8_t c = static_cast<uint8_t>(utf8[i]);
        if (c < 0x80) {
            if (c >= 'A' && c <= 'Z')
                result += static_cast<char>(c + 32);
            else if ((c >= 'a' && c <= 'z') || (c >= '0' && c <= '9') || c == '_' || c == '-' || c == '.')
                result += static_cast<char>(c);
            ++i;
        } else {
            uint32_t cp = 0;
            int bytes = 0;
            if ((c & 0xE0) == 0xC0) { cp = c & 0x1F; bytes = 2; }
            else if ((c & 0xF0) == 0xE0) { cp = c & 0x0F; bytes = 3; }
            else if ((c & 0xF8) == 0xF0) { cp = c & 0x07; bytes = 4; }
            else { ++i; continue; }

            for (int j = 1; j < bytes && (i + j) < utf8.size(); ++j)
                cp = (cp << 6) | (static_cast<uint8_t>(utf8[i + j]) & 0x3F);
            i += bytes;

            if (cp >= 0x4E00 && cp <= 0x9FFF) {
                char ini = pinyin_initial(cp);
                if (ini) result += ini;
            }
        }
    }
    return result;
}

inline bool looks_like_pinyin(const std::string& q) {
    if (q.empty() || q.size() > 30) return false;
    bool has_alpha = false;
    for (char c : q) {
        if (c >= 'a' && c <= 'z') has_alpha = true;
        else if (c != '.' && c != '_' && c != '-') return false;
    }
    return has_alpha;
}

